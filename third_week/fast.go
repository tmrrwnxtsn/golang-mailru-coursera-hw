func FastSearch(out io.Writer) {
	file, err := os.Open(filePath)
	if err != nil {
		panic(err)
	}
	defer file.Close()

	seenBrowsers := make(map[string]bool, 1000)
	foundUsers := strings.Builder{}
	userIdx := -1

	scanner := bufio.NewScanner(file)
	for scanner.Scan() {
		userIdx++

		user := &User{}
		err = user.UnmarshalJSON(scanner.Bytes())
		if err != nil {
			panic(err)
		}

		isAndroid := false
		isMSIE := false

		for _, browser := range user.Browsers {
			if strings.Contains(browser, "Android") {
				isAndroid = true

				if _, found := seenBrowsers[browser]; found {
					continue
				}

				seenBrowsers[browser] = true
			}
			if strings.Contains(browser, "MSIE") {
				isMSIE = true

				if _, found := seenBrowsers[browser]; found {
					continue
				}

				seenBrowsers[browser] = true
			}
		}

		if !(isAndroid && isMSIE) {
			continue
		}

		email := strings.Replace(user.Email, "@", " [at] ", 1)
		foundUsers.WriteString(fmt.Sprintf("[%d] %s <%s>\n", userIdx, user.Name, email))
	}

	_, _ = fmt.Fprintln(out, "found users:\n"+foundUsers.String())
	_, _ = fmt.Fprintln(out, "Total unique browsers", len(seenBrowsers))

	foundUsers.Reset()
}

